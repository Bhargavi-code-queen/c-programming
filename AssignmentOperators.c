/*Program Assignment Operators*/
#include<stdio.h>

int main()
{
    int a;
    a=50;
    a+=50;//Adding 50 to a
    printf("%d\n",a);
    a-=50;//Subtracting 50 from 100
    printf("%d\n",a);
    a*=10;//Multiplying 50*10
    printf("%d\n",a);
    a/=25;//Dividing 500 by 50
    printf("%d\n",a);
    return 0;
}