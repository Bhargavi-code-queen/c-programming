/*Code for Bitwise Operator*/
#include<stdio.h>

int main()
{
    int a,b;
    a=12;//Binary:1100
    b=10;//Binary:1010
    printf("a&b = %d\n",a&b);//AND Operation
    printf("a|b = %d\n",a|b);//OR Operation
    printf("a^b = %d\n",a^b);//XOR Operation
    printf("~a = %d\n",~a);//NOT Operation
    return  0;
}
